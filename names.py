# Simple loop using range()

# A function to print a series of names.
# It does not have any parameters
def printNames():
  for myName in ["Ben", "Laski", "Adam", "Vooris" ]:
        print ( myName )
        upperName = myName.upper()
        print(upperName)

# Call our function
printNames()
